// Função para traduzir texto
function translate(text) {
  if (!text) return text;

  // Se for uma string simples em minúsculas
  const lower = text.toLowerCase();
  if (translations[lower]) {
    // Manter a capitalização original
    if (text[0] === text[0].toUpperCase()) {
      return translations[lower].charAt(0).toUpperCase() + translations[lower].slice(1);
    }
    return translations[lower];
  }

  // Traduzir palavra por palavra
  const words = text.split(' ');
  const translated = words.map(word => {
    const lowerWord = word.toLowerCase();
    // Remove pontuação para tradução
    const cleanWord = lowerWord.replace(/[^\w]/g, '');
    if (translations[cleanWord]) {
      const translatedWord = translations[cleanWord];
      // Manter capitalização
      if (word[0] === word[0].toUpperCase()) {
        return translatedWord.charAt(0).toUpperCase() + translatedWord.slice(1);
      }
      return translatedWord;
    }
    return word; // Mantém palavra original se não houver tradução
  });

  return translated.join(' ');
}

async function processFiles() {
  console.log('Lendo categories.yml...');
  const categoriesYml = fs.readFileSync('c:\\Users\\Administrator\\Downloads\\kit-afeb78ad1f-desktop\\metadata\\categories.yml', 'utf8');
  const categoriesData = yaml.parse(categoriesYml);

  console.log('Lendo icons.json...');
  const iconsJson = fs.readFileSync('c:\\Users\\Administrator\\Downloads\\kit-afeb78ad1f-desktop\\metadata\\icons.json', 'utf8');
  const iconsData = JSON.parse(iconsJson);

  console.log(`Total de categorias: ${Object.keys(categoriesData).length}`);
  console.log(`Total de ícones no JSON: ${Object.keys(iconsData).length}`);

  const result = {
    version: "7.0",
    categories: []
  };

  // Processar cada categoria
  for (const [categoryKey, categoryInfo] of Object.entries(categoriesData)) {
    const categoryLabel = categoryInfo.label || categoryKey;
    const categoryTextPt = categoryTranslations[categoryLabel] || categoryLabel;
    const categoryId = `cat_${categoryKey}`;

    console.log(`\nProcessando categoria: ${categoryLabel} (${categoryTextPt})`);

    const categoryIcons = [];

    // Processar cada ícone da categoria
    if (categoryInfo.icons && Array.isArray(categoryInfo.icons)) {
      for (const iconName of categoryInfo.icons) {
        const iconData = iconsData[iconName];

        if (!iconData) {
          continue;
        }

        // Filtrar apenas ícones com estilo duotone
        if (!iconData.styles || !iconData.styles.includes('duotone')) {
          continue;
        }

        // Traduzir label e keywords
        const originalLabel = iconData.label || iconName;
        const translatedLabel = translate(originalLabel);

        const originalKeywords = iconData.search?.terms || [];
        const translatedKeywords = originalKeywords.map(keyword => translate(keyword));

        const iconObject = {
          id: `fa-duotone fa-${iconName}`,
          text: translatedLabel,
          parentId: categoryId,
          keywords: translatedKeywords
        };

        categoryIcons.push(iconObject);
      }
    }

    if (categoryIcons.length > 0) {
      // Ordenar ícones alfabeticamente pelo text
      categoryIcons.sort((a, b) => a.text.localeCompare(b.text, 'pt-BR'));

      result.categories.push({
        id: categoryId,
        text: categoryTextPt,
        isCategory: true,
        hasChild: true,
        expanded: false,
        icons: categoryIcons
      });

      console.log(`  ✓ ${categoryIcons.length} ícones duotone adicionados (traduzidos)`);
    }
  }

  // Ordenar categorias alfabeticamente pelo text em português
  result.categories.sort((a, b) => a.text.localeCompare(b.text, 'pt-BR'));

  // Estatísticas
  const totalIcons = result.categories.reduce((sum, cat) => sum + cat.icons.length, 0);
  console.log(`\n=== ESTATÍSTICAS ===`);
  console.log(`Total de categorias com ícones duotone: ${result.categories.length}`);
  console.log(`Total de ícones duotone: ${totalIcons}`);

  // Salvar arquivo
  const outputPath = 'C:\\FrotiX\\_FrotiXCompleto 2025 (valendo)\\FrotiX.Site\\fontawesome-icons.json';
  fs.writeFileSync(outputPath, JSON.stringify(result, null, 2), 'utf8');
  console.log(`\n✓ Arquivo gerado com sucesso: ${outputPath}`);
}

