    /// <summary>
    /// Retorna SVG com cores personalizadas
    /// </summary>
    public static string ComCores(string svg, string corPrimary, string corSecondary)
    {
        return svg
            .Replace("#667eea", corPrimary)
            .Replace("#a0aec0", corSecondary);
    }

    /// <summary>
    /// Retorna SVG em tons de cinza (para impressão P&B)
    /// </summary>
    public static string EmCinza(string svg)
    {
        return svg
            .Replace("#667eea", "#374151")
            .Replace("#a0aec0", "#9ca3af");
    }
